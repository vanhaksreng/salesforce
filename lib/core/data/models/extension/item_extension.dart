import 'package:salesforce/core/enums/enums.dart';
import 'package:salesforce/core/utils/helpers.dart';
import 'package:salesforce/realm/scheme/item_schemas.dart';

extension ItemExtension on Item {
  static Item fromMap(Map<String, dynamic> item) {
    return Item(
      Helpers.toStrings(item['no']),
      no2: Helpers.toStrings(item['no_2'] ?? ""),
      description: item['description'],
      description2: Helpers.toStrings(item['description_2']),
      unitPrice: Helpers.formatNumberDb(item['unit_price'] ?? 0),
      inactived: item['inactived'],
      stockUomCode: item['stock_uom_code'] ?? "",
      salesUomCode: item['sales_uom_code'] ?? "",
      preventNegativeInventory: item['prevent_negative_inventory'] ?? "",
      itemTrackingCode: item['item_tracking_code'] ?? "",
      isServiceItem: item['is_service_item'] ?? "No",
      invPostingGroupCode: item["inv_posting_group_code"] ?? "",
      vatProdPostingGroupCode: item["vat_prod_posting_group_code"] ?? "",
      genProdPostingGroupCode: item["gen_prod_posting_group_code"] ?? "",
      itemBrandCode: item["item_brand_code"] ?? "",
      itemCategoryCode: item["item_category_code"] ?? "",
      itemGroupCode: item["item_group_code"] ?? "",
      itemDiscountGroupCode: item["item_discount_group_code"] ?? "",
      picture: item['picture'] ?? "",
      avatar128: item["avatar_128"],
      inventory: Helpers.formatNumberDb(item["inventory"], option: FormatType.quantity),
    );
  }
}

