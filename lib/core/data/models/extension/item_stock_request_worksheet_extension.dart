import 'package:salesforce/core/utils/helpers.dart';
import 'package:salesforce/realm/scheme/sales_schemas.dart';

extension ItemStockRequestWorkSheetExtension on ItemStockRequestWorkSheet {
  static ItemStockRequestWorkSheet fromMap(Map<String, dynamic> json) {
    return ItemStockRequestWorkSheet(
      Helpers.toStrings(json['id']),
      Helpers.toStrings(json['item_no']),
      appId: Helpers.toStrings(json['app_id']),
      fromLocationCode: Helpers.toStrings(json['from_location_code']),
      locationCode: Helpers.toStrings(json['location_code']),
      purchaserCode: Helpers.toStrings(json['purchaser_code']),
      variantCode: Helpers.toStrings(json['variant_code']),
      description: Helpers.toStrings(json['description']),
      description2: Helpers.toStrings(['description_2']),
      unitOfMeasureCode: Helpers.toStrings(json['unit_of_measure_code']),
      qtyPerUnitOfMeasure: Helpers.toDouble(json['qty_per_unit_of_measure'] ?? 1.0),
      orgQuantity: Helpers.toDouble(json['org_quantity'] ?? 0),
      quantity: Helpers.toDouble(json['quantity'] ?? 0),
      quantityBase: Helpers.toDouble(json['quantity_base'] ?? 0),
      quantityToShip: Helpers.toDouble(json['quantity_to_ship'] ?? 0),
      quantityToReceive: Helpers.toDouble(json['quantity_to_receive'] ?? 0),
      quantityShipped: Helpers.toDouble(json['quantity_shipped'] ?? 0),
      quantityReceived: Helpers.toDouble(json['quantity_received'] ?? 0),
      postingDate: Helpers.toStrings(json['posting_date']),
      documentType: Helpers.toStrings(json['document_type']),
      documentNo: Helpers.toStrings(json['document_no']),
      documentLineNo: json['document_line_no'] as String?,
      status: json['status'] as String? ?? 'New',
      backendStatus: json['backend_status'] as String?,
      transferDocumentNo: json['transfer_document_no'] as String?,
      isSync: json['is_sync'] as String? ?? 'Yes',
      createdAt: json['created_at'] as String?,
      updatedAt: json['updated_at'] as String?,
    );
  }

  ItemStockRequestWorkSheet copyWith({
    String? id,
    String? appId,
    String? fromLocationCode,
    String? locationCode,
    String? purchaserCode,
    String? itemNo,
    String? variantCode,
    String? description,
    String? description2,
    String? unitOfMeasureCode,
    double? qtyPerUnitOfMeasure,
    double? orgQuantity,
    double? quantity,
    double? quantityBase,
    double? quantityToShip,
    double? quantityToReceive,
    double? quantityShipped,
    double? quantityReceived,
    String? postingDate,
    String? documentType,
    String? documentNo,
    String? documentLineNo,
    String? status,
    String? backendStatus,
    String? transferDocumentNo,
    String? isSync,
    String? createdAt,
    String? updatedAt,
  }) {
    return ItemStockRequestWorkSheet(
      id ?? this.id,
      itemNo ?? this.itemNo,
      appId: appId ?? this.appId,
      fromLocationCode: fromLocationCode ?? this.fromLocationCode,
      locationCode: locationCode ?? this.locationCode,
      purchaserCode: purchaserCode ?? this.purchaserCode,
      variantCode: variantCode ?? this.variantCode,
      description: description ?? this.description,
      description2: description2 ?? this.description2,
      unitOfMeasureCode: unitOfMeasureCode ?? this.unitOfMeasureCode,
      qtyPerUnitOfMeasure: qtyPerUnitOfMeasure ?? this.qtyPerUnitOfMeasure,
      orgQuantity: orgQuantity ?? this.orgQuantity,
      quantity: quantity ?? this.quantity,
      quantityBase: quantityBase ?? this.quantityBase,
      quantityToShip: quantityToShip ?? this.quantityToShip,
      quantityToReceive: quantityToReceive ?? this.quantityToReceive,
      quantityShipped: quantityShipped ?? this.quantityShipped,
      quantityReceived: quantityReceived ?? this.quantityReceived,
      postingDate: postingDate ?? this.postingDate,
      documentType: documentType ?? this.documentType,
      documentNo: documentNo ?? this.documentNo,
      documentLineNo: documentLineNo ?? this.documentLineNo,
      status: status ?? this.status,
      backendStatus: backendStatus ?? this.backendStatus,
      transferDocumentNo: transferDocumentNo ?? this.transferDocumentNo,
      isSync: isSync ?? this.isSync,
      createdAt: createdAt ?? this.createdAt,
      updatedAt: updatedAt ?? this.updatedAt,
    );
  }
}
